import json
import typing as typ

import fastapi.responses as resp


class JSONResponse(resp.Response):
    media_type = "application/json"

    def render(self, content: typ.Any) -> bytes:
        return json.dumps(
            content,
            ensure_ascii=False,
            allow_nan=False,
            indent=4,
            separators=(", ", ": "),
        ).encode("utf-8")
